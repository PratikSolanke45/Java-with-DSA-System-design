public class Conditional {
    public static void main(String[] args) {
        int age=18;
        
        if(age >=18 && age<60)
        {
            System.out.println("You are in Adult");
        }
        else if(age >= 60)
        {
            System.out.println("You're old man now");
        }
        else{
            System.out.println("you're still a kid");
        }
    }
    
}
